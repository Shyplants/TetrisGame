	static std::array<IVec2, MINO_COUNT> GetShapeBlocks(TetrominoType type, Rotation rot)
	{
		if (!IsMinoType(type))
			return {};

		auto typeIndex = GetMinoShapeIndex(type);
		auto rotIndex = static_cast<int32>(rot);

		return Shapes[typeIndex][rotIndex];
	}
